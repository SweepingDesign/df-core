namespace DreamFactory\Rave\Http\Middleware;


use Closure;
use Illuminate\Contracts\Auth\Guard;

class AdminCheck
{

    /**
     * The Guard implementation.
     *
     * @var Guard
     */
    protected $auth;

    /**
     * Create a new filter instance.
     *
     * @param  Guard  $auth
     * @return void
     */
    public function __construct(Guard $auth)
    {
        $this->auth = $auth;
    }

    /**
     * Handle an incoming request.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \Closure  $next
     * @return mixed
     */
    public function handle($request, Closure $next)
    {
        if(!empty($this->auth->user()) && $this->auth->user()->is_sys_admin)
        {
            return $next($request);
        }
        elseif(!$this->auth->guest())
        {
            return redirect()->guest('rave');
        }
        else{
            return redirect()->guest('rave/auth/login');
        }
    }

}
